// PUBLIC METHOD #################################################

void Pop::Execute(std::vector<IOperand const *> * stack)
{
	if (!stack->empty())
	{
		delete *stack->begin();
		stack->erase(stack->begin());
	}
	else
		throw EmptyStackException();
}

